package sanitizer

type set struct {
	m map[string]bool
}

func sset(vals []string) set {
	m := make(map[string]bool)
	for _, val := range vals {
		m[val] = true
	}
	return set{m: m}
}

func (s *set) has(val string) bool {
	_, ok := s.m[val]
	return ok
}

